    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(!jTextField1.getText().equals("")){
            SingleAlias d = new SingleAlias();
            d.actualName = jTextField1.getText();
            parent.aliasObj.list.addElement(d);
            parent.WriteToFile();
            parent.readFromFile();
            fillMainList();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if(!jTextField2.getText().equals("")){
            if(!jComboBox1.getSelectedItem().toString().equals("")){
                int index = jComboBox1.getSelectedIndex();
                parent.aliasObj.list.elementAt(index).alias.add(jTextField2.getText());
                parent.WriteToFile();
                parent.readFromFile();
                fillSubList();
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed


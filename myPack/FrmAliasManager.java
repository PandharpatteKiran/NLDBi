    private void fillMainList(){
        DefaultListModel lm= new DefaultListModel();
        DefaultComboBoxModel cm = new DefaultComboBoxModel();
        for(int i=0;i<parent.aliasObj.list.size();i++){
            lm.addElement(parent.aliasObj.list.elementAt(i).actualName);
            cm.addElement(parent.aliasObj.list.elementAt(i).actualName);
        }
        jList1.setModel(lm);
        jComboBox1.setModel(cm);
    }

    private void fillSubList(){
        int index = jList1.getSelectedIndex();
        DefaultListModel lm= new DefaultListModel();
        if(index >= 0){
            for(int i=0;i<parent.aliasObj.list.elementAt(index).alias.size();i++){
                lm.addElement(parent.aliasObj.list.elementAt(index).alias.elementAt(i));
            }
        }
        jList3.setModel(lm);
    }

